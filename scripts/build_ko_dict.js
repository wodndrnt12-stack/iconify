// Build the new ko_dict entries (Korean → [English])
// ============================================================

// Load existing words
console.log('Loading icon_words.json...');
const iconWords = JSON.parse(fs.readFileSync(WORDS_PATH, 'utf8'));
const validWords = new Set(iconWords.map(w => w.word));
console.log(`Valid icon words: ${validWords.size}`);

// Load existing ko_dict
console.log('Loading existing ko_dict.json...');
const existingKoDict = JSON.parse(fs.readFileSync(KO_DICT_PATH, 'utf8'));
console.log(`Existing ko_dict entries: ${Object.keys(existingKoDict).length}`);

// Build Korean→English from EN_KO (reverse mapping)
// For each English word that exists in icon DB, collect Korean terms
const koToEn = new Map();

for (const [enWord, koreanTerms] of Object.entries(EN_KO)) {
  // Only add if this English word actually exists in icon names
  if (!validWords.has(enWord)) continue;

  for (const koTerm of koreanTerms) {
    if (!koToEn.has(koTerm)) {
      koToEn.set(koTerm, new Set());
    }
    koToEn.get(koTerm).add(enWord);
  }
}

console.log(`New Korean terms from reverse mapping: ${koToEn.size}`);

// Merge: existing ko_dict takes priority, add new entries
const merged = { ...existingKoDict };
let added = 0;
let enriched = 0;

for (const [koTerm, enSet] of koToEn.entries()) {
  const enWords = [...enSet];

  if (koTerm in merged) {
    // Enrich existing entry with any missing English words
    const existing = new Set(merged[koTerm]);
    const beforeSize = existing.size;
    for (const w of enWords) {
      existing.add(w);
    }
    if (existing.size > beforeSize) {
      merged[koTerm] = [...existing];
      enriched++;
    }
  } else {
    // Add new entry
    merged[koTerm] = enWords;
    added++;
  }
}

console.log(`Added new entries: ${added}`);
console.log(`Enriched existing entries: ${enriched}`);
console.log(`Total merged entries: ${Object.keys(merged).length}`);

// Sort by Korean key for readability
const sorted = {};
for (const key of Object.keys(merged).sort()) {
  sorted[key] = merged[key];
}

fs.writeFileSync(KO_DICT_PATH, JSON.stringify(sorted, null, 2), 'utf8');
console.log(`\nSaved: ${KO_DICT_PATH}`);
console.log(`Final ko_dict entries: ${Object.keys(sorted).length}`);
